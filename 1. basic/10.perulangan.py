# Iteration for
print("Demo Iteration - for")
for i in range(1, 5):
    print(i)

# Nested For
print("Demo nested - for")
for i in range(1, 3):
    for j in range(5, 10):
        print(str(i) + " - " + str(j))

# Iteration while
print("Demo Iteration - while")
i = 0
while (i < 10):
    print(i)
    i += 1
